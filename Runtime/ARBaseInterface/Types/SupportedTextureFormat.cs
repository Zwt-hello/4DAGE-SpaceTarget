
namespace SpaceTarget.Runtime
{
    /// <summary>
    /// Supported color texture format
    /// </summary>
    public enum SupportedTextureFormat
    {
        /// <summary>
        /// Color with alpha texture format, 8-bits per channel.
        /// </summary>
        RGBA32 = 0,

        /// <summary>
        /// Color texture format, 8-bits per channel.
        /// </summary>
        RGB24 = 1
    }
}