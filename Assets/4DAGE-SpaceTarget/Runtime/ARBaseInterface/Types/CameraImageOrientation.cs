
namespace SpaceTarget.Runtime
{
    /// <summary>
    /// Raw image orientation type
    /// </summary>
    public enum CameraImageOrientation 
    {
        /// <summary>
        /// No rotate image
        /// </summary>
        NONE = 0,

        /// <summary>
        /// Upside down image
        /// </summary>
        UPSIDE_DOWN = 1,

        /// <summary>
        /// Rotate left image
        /// </summary>
        LEFT = 2,

        /// <summary>
        /// Rotate right image
        /// </summary>
        RIGHT = 3
    }
}